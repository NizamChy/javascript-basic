// switch
// switch is faster than if-else
// switch checks exact the given value
// where if-else check every condition
const color = "blue";
switch (color) {
  case "green":
    console.log("The color is green");
    break;
  case "blue":
    console.log("The color is blue");
    break;
  case "red":
    console.log("The color is red");
    break;
  default:
    console.log("I dont know this color");
}

