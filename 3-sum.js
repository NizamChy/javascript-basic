var num1 = 5;
var num2 = 10;

console.log("Number 1 = ", num1);
console.log("Number 2 = ", num2);

// Addition
var add = num1+num2;
console.log("Addition = ",add);

// Substraction
var sub = num2-num1;
console.log("Substraction = ",sub);

// Multiplication
var mul = num1*num2;
console.log("Multiplication = ",mul);

// Division
var div = num2/num1;
console.log("Division = ",div);

// Remainder
var mod = num2%num1;
console.log("Remainder = ",mod);



